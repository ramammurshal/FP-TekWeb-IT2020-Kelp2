            <h2>Built-in Pomodoro</h2>
            <p>
            The Pomodoro Technique is a time management method developed by Francesco Cirillo in the late 
             1980s. The technique uses a timer to break down work into intervals, traditionally 25 minutes in 
             length, separated by short breaks — definition from Wikipedia.<br><br>

            Out of all the productivity techniques out there, the Pomodoro technique is probably the easiest 
             to implement and has significant results. Chris Winfield, a writer, and entrepreneur could get 40 hours 
             of work done in 16.7 hours using this technique.
            </p>

           <h3>25-minute sessions</h3>
           <p>Let’s see why the 25-minute sessions of Pomodoro are important.</p>
           <ul>
                <li>
                Distractions — According to psychologists Matthew A. Killingsworth and Daniel T. Gilbert of 
                Harvard University, people spend 46.9 percent of their waking hours thinking about 
                something other than what they’re doing. Thoughts are one thing but nowadays we are 
                surrounded by all sorts of visual and auditory cues which distract us from what really matters.
                </li>
                <li>
                Boredom — According to research by Madelon L. M. and Edwin A. J, employees with high 
                work centrality are prone to experience diminished well-being as a consequence 
                of boredom, whereas fulfilling one’s basic psychological needs on a daily basis after work 
                mitigates these negative effects.
                </li>
                <li>
                Physical health — Three different studies have shown that excessive time spent sitting at 
                work may increase the risk of cardiovascular disease, obesity, diabetes, and all‐cause 
                mortality. Also, we all know that sitting for long can cause back and neck problems.
                </li>
           </ul>
           <p>The 25-minute sessions seize your attention and knowing that you will get a break afterward, motivates you to stay focused.</p>

           <h3>5-minute breaks</h3>
           <p>You can use the 5-minute breaks between sessions in several ways depending on your situation.</p>
           <ul>
                <li>
                Figuring out what to do — According to a McKinsey Survey, on average, respondents spent 
                37 percent of their time making decisions, and more than half of this time was thought to be 
                spent ineffectively. A lot of times while working, we get stuck between to-do lists and take 
                the mental toll of tasks we haven’t even begun.
                </li>
                <li>
                Exercise — If you are feeling tired of work or can hear your bones pop, then a little 
                stretching is probably the best way to spend the break time.
                </li>
                <li>
                Reward — You can give yourself a reward such as a desert, or use Twitter or YouTube during 
                the break — I would not recommend this because you may postpone your next session. Any 
                form of reward will keep you motivated.
                </li>
           </ul>
           <p>
            The Pomodoro technique is the most common productivity technique out there because it works. 
             The problem is the distraction caused by these Pomodoro timers which are usually on our phones, 
             and once you touch your phone, your brain is simply hijacked by world-class marketers through 
             notifications or our own temptation to check your feed. ‘This will only take a few seconds’ turns into 
             minutes or hours.<br><br>
            
            Flow State has a built-in Pomodoro timer. It consists of two 30 minute playlists of music followed by 
             5-minute breaks to allow you to plan your next session, reflect on your work, or do some stretching.<br><br>

            This is great because you won’t have to worry about the next song, set timers, and you can let go of 
             all your tracking apps, timers, and music playlists. You just need to start the podcast and all the 
             above is taken care of.
            </p>

            <h3>Binaural Beats</h3>
            <p>
            The 5-minute break is a binaural sound wave to help you maintain focus. As mentioned earlier, 
             during this break-time you can do several things to ease your mind, but I would recommend you to 
             reflect on your work or plan for the next session. If you are tired, take a longer break.<br><br>

            As for the effectiveness of binaural beats, here’s what I could find-<br><br>

            According to the research by L.S. Colzato, high-frequency binaural beats did have a significant impact 
             on the global precedence effect: the precedence effect became smaller, suggesting that visual 
             attention became more focused than in the control condition.<br><br>

            Global precedence effect means the global aspects of a scene or object in question are processed 
             before the local aspects. If global precedence becomes small, it means the focus has increased.
            </p>   
